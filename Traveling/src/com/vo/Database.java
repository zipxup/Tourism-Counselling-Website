package com.vo;

public class Database {
	private String name;
	private String pop;
	private String informaction;
	private String time;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPop() {
		return pop;
	}

	public void setPop(String pop) {
		this.pop = pop;
	}

	public String getInformaction() {
		return informaction;
	}

	public void setInformaction(String informaction) {
		this.informaction = informaction;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

}
